// This class demonstrates static methods.

public class Metric
{
   // This method converts a distance in miles to kilometers.
   public static double milesToKilometers(double m)
   {
      return m * 1.609;
   }

   // This method converts a distance in kilometers to miles.
   public static double kilometersToMiles(double k)
   {
      return k / 1.609;
   }
}
